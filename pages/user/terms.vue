const isRtl = computed(() => ['he-IL', 'ar-SA'].includes(activeLanguage.value));
watchEffect(() => {
  if(!process.client) return
  document?.body?.setAttribute("dir", isRtl.value ? "rtl" : "ltr");
  document?.documentElement?.setAttribute("dir", isRtl.value ? "rtl" : "ltr");
});
</script>

